namespace BluDay.Net.UI.Appearance;

/// <summary>
/// Represents different kinds of application themes.
/// </summary>
public enum AppTheme
{
    /// <summary>
    /// Represents the system default theme.
    /// </summary>
    System,

    /// <summary>
    /// Represents the light theme.
    /// </summary>
    Light,

    /// <summary>
    /// Represents the dark theme.
    /// </summary>
    Dark
};