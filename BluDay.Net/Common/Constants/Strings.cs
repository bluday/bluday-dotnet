namespace BluDay.Net.Common.Constants;

/// <summary>
/// Provides a collection of convenient <see cref="string"/> constants for common
/// string values.
/// </summary>
public static class Strings
{
    /// <summary>
    /// Represents the long command-line argument flag prefix ("--").
    /// </summary>
    public const string ARG_LONG_FLAG_PREFIX = "--";

    /// <summary>
    /// Represents the short command-line argument flag prefix ("-").
    /// </summary>
    public const string ARG_SHORT_FLAG_PREFIX = "-";

    /// <summary>
    /// Represents the identifier for separating positional command-line arguments
    /// with optional arguments ("--").
    /// </summary>
    public const string POSITIONAL_ARG_SYMBOL = "--";

    /// <summary>
    /// Represents the default string value ("Default").
    /// </summary>
    public const string Default = "Default";

    /// <summary>
    /// Represents the "None" string value ("None").
    /// </summary>
    public const string None = "None";

    /// <summary>
    /// Represents the "Untitled" string value ("Untitled").
    /// </summary>
    public const string Untitled = "Untitled";

    /// <summary>
    /// Represents the "View" string value ("View").
    /// </summary>
    public const string View = "View";

    /// <summary>
    /// Represents the "ViewModel" string value ("ViewModel").
    /// </summary>
    public const string ViewModel = "ViewModel";
}
